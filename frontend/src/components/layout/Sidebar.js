const STORAGE_KEY = 'clawmander-sidebar-collapsed';

export default function Sidebar({ mobileOpen, onMobileClose }) {
  const router = useRouter();
  const [collapsed, setCollapsed] = useState(true);
  const [mounted, setMounted] = useState(false);
  const [enableTransition, setEnableTransition] = useState(false);
  const initializedRef = useRef(false);

  useEffect(() => {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored !== null) {
      setCollapsed(stored === 'true');
    } else {
      setCollapsed(window.innerWidth < 1024);
    }
    setMounted(true);

    // Enable transitions only after the correct state has been painted,
    // so the initial localStorage read doesn't cause a visible animation.
    if (!initializedRef.current) {
      initializedRef.current = true;
      requestAnimationFrame(() => {
        requestAnimationFrame(() => {
          setEnableTransition(true);
        });
      });
    }
  }, []);

  const toggle = () => {
    const next = !collapsed;
    setCollapsed(next);
    localStorage.setItem(STORAGE_KEY, String(next));
  };

  const handleNavClick = () => {
    if (mobileOpen) onMobileClose();
  };

  return (
    <>
      {/* Mobile overlay backdrop */}
      {mobileOpen && (
        <div
          className="fixed inset-0 bg-black/50 z-40 md:hidden"
          onClick={onMobileClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`
          ${collapsed ? 'w-[4.5rem]' : 'w-56'}
          bg-surface-light border-r border-gray-800 flex flex-col py-4
          ${enableTransition ? 'transition-all duration-200' : ''}
          flex-shrink-0 overflow-hidden
          hidden md:flex
          ${mobileOpen ? '!flex fixed inset-y-0 left-0 z-50 w-52' : ''}
        `}
      >
        <nav className="flex-1 flex flex-col gap-1 px-2">
          {NAV_ITEMS.map(({ href, label, icon }) => {
            const active = href === '/' ? router.pathname === '/' : router.pathname.startsWith(href);
            return (
              <Link key={href} href={href} className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-base font-medium cursor-pointer transition-colors whitespace-nowrap ${
                  active
                    ? 'bg-accent text-white'
                    : 'text-gray-500 hover:text-white hover:bg-surface-lighter'
                }`}
                title={collapsed && !mobileOpen ? label : undefined}
                onClick={handleNavClick}
              >
                <span className="flex-shrink-0" style={{ width: 22, height: 22 }}>{icon}</span>
                {((!collapsed && mounted) || mobileOpen) && (
                  <span className="truncate">{label}</span>
                )}
              </Link>
            );
          })}
        </nav>

        <div className="px-2 mt-2 hidden md:block">
          <button
            onClick={toggle}
            className="w-full flex items-center justify-center gap-2 rounded-lg px-3 py-2 text-gray-500 hover:text-white hover:bg-surface-lighter transition-colors"
            title={collapsed ? 'Expand sidebar' : 'Collapse sidebar'}
          >
            <svg
              width="16"
              height="16"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth={2}
              className={`transition-transform duration-200 ${collapsed ? 'rotate-180' : ''}`}
            >
              <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 19.5L8.25 12l7.5-7.5" />
            </svg>
            {!collapsed && mounted && (
              <span className="text-xs">Collapse</span>
            )}
          </button>
        </div>
      </aside>
    </>
  );
}
